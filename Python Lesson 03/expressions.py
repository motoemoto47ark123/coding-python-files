# Examples of expressions in Python

# Example of a simple expression
a = 5
b = 10
c = a + b  # This is an expression that adds a and b
print(f"a + b = {c}")

# Example of string expressions
first_name = "John"
last_name = "Doe"
full_name = first_name + " " + last_name  # Expression that concatenates strings
print(f"Full name: {full_name}")

# Example of expressions with comparisons (returns boolean value)
x = 5
y = 10
result = x < y  # Expression that evaluates to True or False
print(f"Is {x} less than {y}? {result}")

# Complex expression with multiple operations
complex_result = (a + b) * 2 / (y - x)
print(f"Complex expression result: {complex_result}")

# Expressions can also involve function calls
import random
random_value = random.randint(1, 100)
print(f"Random number expression: {random_value}")

