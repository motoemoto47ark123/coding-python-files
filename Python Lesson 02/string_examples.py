# String examples
# Defining a string
cheese = 'hello'
print(cheese)

# String concatenation examples
print('bacon' + 'eggs')
print('bacon ' + 'eggs')

# More advanced string concatenation 
first_name = 'John'
last_name = 'Smith'
full_name = first_name + ' ' + last_name
print('Full name: ' + full_name)

# Showing different things that can go in strings
print('Letters, numbers, and symbols: abc123!@#$%^&*()')

# Demonstrating string variables and operations
greeting = 'Hello'
target = 'World'
message = greeting + ' ' + target + '!'
print(message) 