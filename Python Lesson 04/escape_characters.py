# Learning Target 2: Understanding how to use escape characters in Python
print("Backslash: \\")  # Prints a single backslash
print("Single quote: \'")  # Prints a single quote
print("Double quote: \"")  # Prints a double quote
print("Newline:\nSecond line")  # Creates a new line
print("Tab:\tIndented")  # Creates a tab space

# Example with astronaut's cavity
print("What do dentists call an astronaut\'s cavity?")

# Example showing the issue with tabs
print("I could go with this\tthat on the topic")  # \t creates a tab 